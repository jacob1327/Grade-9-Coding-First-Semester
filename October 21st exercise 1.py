side_of_square = int(input("Enter the side length of the square: "))
Area_of_square = side_of_square ** 2
print(f"The area of the square is {Area_of_square}.")

length_of_rectangle = int(input("Enter the length of the rectangle: "))
width_of_rectangle = int(input("Enter the width of the rectangle: "))
Area_of_rectangle = length_of_rectangle * width_of_rectangle
print(f"The area of the rectangle is {Area_of_rectangle}.")

radius_of_circle = int(input("Enter the radius of the circle: "))
Area_of_circle = 3.141592 * radius_of_circle ** 2
print(f"The area of the circle is {Area_of_circle}.")

base_of_triangle = int(input("Enter the base length of the triangle: "))
height_of_triangle = int(input("Enter the height of the triangle: "))
Area_of_triangle = base_of_triangle * height_of_triangle / 2
print(f"The area of the triangle is {Area_of_triangle}.")

